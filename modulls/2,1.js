function countryInfo(country, population, capitalCity) {
  console.log(
    `${country} has ${population} million people and its capital city is ${capitalCity}`
  );
}

const country = "Spain";
const capitalCity = "Madrid";
const country1 = "England";
const capitalCity1 = "London";
const country2 = "Israel";
const capitalCity2 = "Jerusalem";

countryInfo(country, 46, capitalCity);
countryInfo(country1, 12, capitalCity1);
countryInfo(country2, 11, capitalCity2);
